int		ft_atoi(char *str)
{
	int a;
	int b;

	a = 0;
	while (*str == ' ' || *str == '\n' || *str == '\t'
			|| *str == '\v' || *str == '\f' || *str == '\r')
		str++;
	if (*str == '-')
		b = 1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str != '\0' && *str >= '0' && *str <= '9')
	{
		a = (a * 10) + (*str - 48);
		str++;
	}
	if (b == 1)
		return (-a);
	else
		return (a);
}
