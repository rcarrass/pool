int		ft_str_is_alpha(char *str)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if ((str[i] >= 0 && str[i] <= 64) || (str[i] >= 91 && str[i] <= 96)
				|| (str[i] >= 123 && str[i] <= 127))
			return (0);
		else
			i++;
	}
	return (1);
}
