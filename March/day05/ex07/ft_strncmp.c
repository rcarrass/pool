int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	int				j;
	int				k;

	i = 0;
	j = 0;
	if (*s1 || *s2)
	{
		while (s1[i] == s2[i] && n > i)
			i++;
		j = s1[i];
		k = s2[i];
		return (j - k);
	}
	return (0);
}
