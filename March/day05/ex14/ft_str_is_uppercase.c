int		ft_str_is_uppercase(char *str)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if ((str[i] >= 0 && str[i] <= 64) || (str[i] >= 91 && str[i] <= 127))
			return (0);
		else
			i++;
	}
	return (1);
}
