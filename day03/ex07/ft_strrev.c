void ft_putchar(char c);

char	 *ft_strrev(char *str)
{
	int l;
	int i;

	i = 0;
	l = -1;
	while (*str++)
		l++;
	while (l >= 0)
	{
		str[i]= str[l];
		l--;
		i++;
	}
	return (str);
}
